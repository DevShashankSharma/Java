
public class if_else {
    public static void main(String[] args) {
        // if else statement
        int x = 10;
        int y = 20;
        if (x > y) {
            System.out.println("x is greater than y");
        } else if (x < y) {
            System.out.println("x is less than y");
        } else {
            System.out.println("x is equal to y");
        }
    }
}